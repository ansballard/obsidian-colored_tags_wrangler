export const removeById = (id:string) :void => {
	if (!id.startsWith("#")){
		throw new DOMException("id did not start with a '#' ")
	}
	const existingStyle = document.querySelector(id)
	if (existingStyle) {
		existingStyle.remove();
	}
}

